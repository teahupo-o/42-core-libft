int	ft_atoi(char *str)
{
	int	i;
	int	j;
	int	final;

	i = 0;
	j = 1;
	final = 0;
	while (str[i] <= ' ')
	{
		i++;
	}
	if (str[i] == 45 || str[i] == 43)
	{	
		if (str[i] == 45 )
		{
			j = j * -1;
		}
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		final = final * 10 + (str[i] - 48);
		i++;
	}
	final = final * j;
	return (final);
}
